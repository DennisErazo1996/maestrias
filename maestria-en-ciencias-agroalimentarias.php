                    <h4 class="uk-heading-bullet">Perfil de ingreso</h4>
                    <p>El programa está dirigido a profesionales que estén en posesión del grado académico de licenciatura otorgado por una universidad nacional o extranjera, debidamente reconocido por la Dirección de Educación Superior de Honduras, en el área agroalimentaria o afines tales como; agronomía, agroindustria, nutrición, administración, economía y finanzas, veterinaria, biología, biotecnología y química. Además, el postulante deberá demostrar interés por la investigación y/o innovación, así como conocimiento básico del idioma inglés.</p>
                    <h4 class="uk-heading-bullet">Perfil de egreso</h4>
                    <p>El graduado de la maestría en Ciencias Agroalimentarias se caracterizará por su capacidad de desarrollar un pensamiento crítico, analítico y creativo en torno a conocimientos avanzados sobre estas ciencias, que le permitirá abordar de manera independiente e innovadora, problemas propios de su campo laboral y académico. Además, estará capacitado para realizar investigación científica e innovación científico-tecnológica en el área de Agroindustria y Agronegocios. El graduado de esta maestría contará con las siguientes competencias genéricas:</p>
                    <ul class="uk-list uk-list-square">
                        <li>Resuelve problemas en su campo profesional, identificando y desarrollando opciones de solución.</li>
                        <li>Emprende e innova proyectos profesionales con liderazgo e innovación.</li>
                        <li>Realiza investigación científica y participativa en su campo profesional.</li>
                        <li>Expresa ideas, argumentos y mensajes de manera clara, coherente y precisa haciendo uso del lenguaje adecuado de acuerdo con el contexto en que se desenvuelva.</li>
                        <li>Utiliza las Tecnologías de la Información y Comunicación (TIC) de forma eficiente. </li>
                        <li>Trabaja en equipos proactivamente.</li>
                    </ul><br>
                    <p>A partir de este perfil general, el Programa define cuatro (4) sub perfiles de graduados:</p>
                    <ul data-uk-accordion>
                        <li>
                            <a class="uk-accordion-title uk-padding-small fondo-verde-claro texto-verde" href="#">Orientación Agroindustria – Profesionalizante </a>
                            <div class="uk-accordion-content uk-padding fondo-verde-claro">
                                <p>El graduado de este sub perfil contará con una formación sólida, competencias y conocimientos específicos en las áreas de manejo poscosecha, procesamiento de alimentos y calidad e inocuidad agroalimentaria; lo cual le permitirá el ejercicio profesional dentro del ámbito de la agroindustria, por lo que podrá solucionar problemas complejos del área profesional y generar innovación que permita optimizar la producción agroalimentaria utilizando adecuadamente los recursos naturales. Este profesional tendrá las siguientes competencias específicas:</p>
                                <ul class="uk-list uk-list-square">
                                    <li>Gestiona e implementa sistemas de calidad e inocuidad para garantizar la seguridad de los alimentos procesados.</li>
                                    <li>Desarrolla e innova tecnologías, métodos y productos alimenticios con propiedades funcionales para el mercado nacional e internacional.</li>
                                    <li>Optimiza procesos de conservación de alimentos para incrementar la productividad del sector agroalimentario.</li>
                                    <li>Comprende la literatura científica y aplica los resultados expuestos en la misma para la solución de problemas actuales del sector.</li>
                                </ul>
                            </div>
                        </li>
                        <li>
                            <a class="uk-accordion-title uk-padding-small fondo-verde-claro texto-verde" href="#">Orientación Agroindustria – Académica</a>
                            <div class="uk-accordion-content uk-padding fondo-verde-claro">
                                <p>El graduado de este sub perfil contará con una formación sólida, competencias y conocimientos específicos en las áreas de manejo poscosecha, procesamiento de alimentos y calidad e inocuidad agroalimentaria; lo cual le permitirá el ejercicio profesional y académico dentro del ámbito de la agroindustria, desarrollando la capacidad de participar en actividades de investigación, a través de la utilización del método científico, análisis crítico y realización de propuestas creativas. Este profesional tendrá las siguientes competencias específicas:</p>
                                <ul class="uk-list uk-list-square">
                                    <li>Gestiona e implementa sistemas de calidad e inocuidad para garantizar la seguridad de los alimentos procesados.</li>
                                    <li>Desarrolla e innova tecnologías, métodos y productos alimenticios con propiedades funcionales para el mercado nacional e internacional.</li>
                                    <li>Optimiza procesos de conservación de alimentos para incrementar la productividad del sector agroalimentario.</li>
                                    <li>Comprende la literatura científica y aplica los resultados expuestos en la misma para la solución de problemas actuales del sector.</li>
                                    <li>Genera propuestas de investigación aplicada que se apoye en métodos rigurosos de razonamiento.</li>
                                    <li>Lidera y/o participa en proyectos de investigación aplicada y desarrollo tecnológico que impliquen la transferencia de nueva tecnología hacia el sector agroalimentario.</li>
                                </ul>
                            </div>
                        </li>
                        <li>
                            <a class="uk-accordion-title uk-padding-small fondo-verde-claro texto-verde" href="#">Orientación Agronegocios – Profesionalizante</a>
                            <div class="uk-accordion-content uk-padding fondo-verde-claro">
                                <p>El graduado de este sub perfil contará con una formación sólida, competencias y conocimientos específicos en las áreas de innovación en agronegocios, liderazgo y calidad empresarial y organización empresarial; lo cual le permitirá el ejercicio profesional dentro del ámbito de los agronegocios, por lo que podrá solucionar problemas complejos del área profesional, pero también diseñar, ejecutar y controlar modelos de negocios, a través del análisis de los factores productivos. Este profesional tendrá las siguientes competencias específicas:</p>
                                <ul class="uk-list uk-list-square">
                                    <li>Administra cadenas de valor y suministro con el fin de buscar mecanismos para mejorar la eficiencia y eficacia de las mismas.</li>
                                    <li>Analiza procesos de producción, identifica tendencias, reconoce problemáticas y a la vez propone alternativas de solución que van desde el mejor uso de los recursos, hasta el diseño y desarrollo de estrategias de agronegocios, que permitan a las empresas posicionarse en mercados más competitivos.</li>
                                    <li>Gerencia programas y proyectos agroalimentarios en empresas privadas, ONGs, gobiernos y cooperantes para contribuir al desarrollo del país. </li>
                                    <li>Identifica y ejecuta proyectos de inversión factibles, oportunos y alcanzables que generen confiabilidad entre los participantes de la cadena agroalimentaria.</li>
                                    <li>Desarrolla la gestión competitiva en agronegocios a fin de perfilar, examinar, alinear y detallar de forma eficiente y eficaz las estrategias, políticas y acciones de una empresa y/o organización.</li>
                                </ul>
                            </div>
                        </li>
                        <li>
                            <a class="uk-accordion-title uk-padding-small fondo-verde-claro texto-verde" href="#">Orientación Agronegocios – Académica</a>
                            <div class="uk-accordion-content uk-padding fondo-verde-claro">
                                <p>El graduado de este sub perfil contará con una formación sólida, competencias y conocimientos específicos en las áreas de innovación en agronegocios, liderazgo y calidad empresarial y organización empresarial; lo cual le permitirá el ejercicio profesional y académico dentro del ámbito de los agronegocios. El graduado de este sub perfil podrá diseñar, ejecutar y controlar modelos de negocios, a través del análisis de los factores productivos; pero también desarrollará la capacidad de participar en actividades de investigación, a través de la utilización del método científico, análisis crítico y realización de propuestas creativas. Este profesional tendrá las siguientes competencias específicas:</p>
                                <ul class="uk-list uk-list-square">
                                    <li>Administra cadenas de valor y suministro con el fin de buscar mecanismos para mejorar la eficiencia y eficacia de las mismas.</li>
                                    <li>Analiza procesos de producción, identifica tendencias, reconoce problemáticas y a la vez propone alternativas de solución que van desde el mejor uso de los recursos, hasta el diseño y desarrollo de estrategias de agronegocios, que permitan a las empresas posicionarse en mercados más competitivos.</li>
                                    <li>Gerencia programas y proyectos agroalimentarios en empresas privadas, ONGs, gobiernos y cooperantes para contribuir al desarrollo del país.</li>
                                    <li>Identifica y ejecuta proyectos de inversión factibles, oportunos y alcanzables que generen confiabilidad entre los participantes de la cadena agroalimentaria.</li>
                                    <li>Desarrolla la gestión competitiva en agronegocios a fin de perfilar, examinar, alinear y detallar de forma eficiente y eficaz las estrategias, políticas y acciones de una empresa y/o organización.</li>
                                    <li>Desarrolla, lidera y/o participa en proyectos de investigación científica para mejorar procesos de las empresas y/o instituciones dedicadas a los agronegocios y afines.</li>
                                    <li>Transfiere conocimientos científicos y/o tecnológicos en el contexto socioeconómico de las empresas o instituciones con las que se relaciona en su desempeño profesional.</li>
                                    <li>Desarrolla propuestas de investigación aplicada que se apoye en métodos rigurosos de razonamiento.</li>
                                </ul>
                            </div>
                        </li>
                    </ul><br>
                    <h4 class="uk-heading-bullet">Perfil ocupacional/Mercado laboral </h4>
                    <p>De forma general, el graduado podrá desempeñarse laboralmente en las siguientes áreas:</p>
                    <ul class="uk-list uk-list-square">
                        <li>
                            <strong>Graduado de la Maestría Profesionalizante:</strong><br>
                            <p>Podrá laborar en Empresas del Sector Agroalimentario y solucionar problemas complejos de su área profesional (Agroindustria o Agronegocios), generando innovación que permita optimizar la producción agroalimentaria de una forma amigable con el ambiente.</p>
                        </li>
                        <li>
                            <strong>Graduado de la Maestría Académica:</strong><br>
                            <p>Podrá laborar en Empresas del Sector Agroalimentario, pero también en Instituciones Educativas y de Investigación. Contará con formación, conocimientos y competencias sólidas que le permitirán el ejercicio académico e investigativo en su área profesional (Agroindustria o Agronegocios).</p>
