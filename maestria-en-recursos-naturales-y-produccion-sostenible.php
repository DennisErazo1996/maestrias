                <li>
                    <h4 class="uk-heading-bullet">Objetivo general</h4>
                    <p>Formar profesionales con enfoque científico tecnológico en los Recursos Naturales y la Producción Sostenible, que propongan soluciones innovadoras, pertinentes y acertadas ante problemática en el área de formación.</p>
                    <h4 class="uk-heading-bullet">Objetivos específicos</h4>
                    <ul class="uk-list uk-list-square">
                        <li>Generación de investigaciones de impacto con una visión holística de las necesidades y problemas de las comunidades rurales que contribuyan a la toma de decisiones acertadas y pertinentes para la integración entre la conservación del ambiente y la producción.</li>
                        <li>Formar profesionales con capacidad para incidir en políticas públicas para la producción y la gestión de los recursos naturales en la búsqueda de la seguridad y soberanía alimentaria en el país.</li>
                        <li>Contribuir a elevar las capacidades de los profesionales en Honduras, en el ámbito de la producción sostenible para el logro de las metas planteadas en los Objetivos del Desarrollo Sostenible y a cumplir con los compromisos de país en la Contribuciones Nacionalmente Determinadas.</li>
                        <li>Proponer técnicas, tecnologías y procesos alternativos para los sistemas productivos y agroalimentarios resilientes al cambio climático enfocados hacia el desarrollo humano y económico sostenible.</li>
                    </ul>
                </li>
                <li>
                    <h4 class="uk-heading-bullet">Perfil de ingreso</h4>
                    <p>El programa de <strong>Maestría en Recursos Naturales y Producción Sostenible</strong> está dirigido a profesionales en el área de las ciencias biológicas, agropecuarias, ambientales, sociales, económicas, forestales y afines, que cumplan los siguientes requisitos, basados en el artículo 58 del reglamento de posgrados:</p>
                    <ul class="uk-list uk-list-square">
                        <li>Título de licenciatura en las áreas afines debidamente reconocido o incorporado de acuerdo con lo establecido en la Ley de Educación Superior.</li>
                        <li>Índice académico de 70% como mínimo.</li>
                        <li>Documento de Identificación Nacional (DNI) o carné de residentes.</li>
                        <li>Conocimientos básicos de informática.</li>
                        <li>Comprensión del idioma inglés.</li>
                        <li>Destrezas académicas básicas: habilidad para el manejo de verbal y escrito en idioma español y organización lógica de ideas.</li>
                        <li>Exposición de motivos para ingresar al posgrado</li>
                    </ul>

                    <h4 class="uk-heading-bullet">Perfil de egreso</h4>
                    <p class="uk-text-bold">Competencias genéricas</p>
                    <ul class="uk-list uk-list-square">
                        <li>Resuelve problemas en su campo profesional, identificando y desarrollando opciones de solución.</li>
                        <li>Emprende e innova proyectos profesionales con liderazgo.</li>
                        <li>Realiza investigación científica y participativa en su campo profesional.</li>
                        <li>Expresa ideas, argumentos y mensajes de manera clara, coherente y precisa haciendo uso del lenguaje adecuado de acuerdo con el contexto en que se desenvuelva.</li>
                        <li>Utiliza las Tecnologías de la Información y Comunicación (TIC) de forma eficiente.</li>
                        <li>Trabaja en equipos proactivamente.</li>
                    </ul>
                    <p class="uk-text-bold">Competencias específicas</p>
                    <ul class="uk-list uk-list-square">
                        <li>Redacta documentos técnico científicos.</li>
                        <li>Construye propuestas para el manejo integral de microcuencas.</li>
                        <li>Diseña estrategias agroecológicas para el manejo de suelos tropicales para la producción sostenible.</li>
                        <li>Lidera procesos de desarrollo rural.</li>
                        <li>Formula proyectos y planes para desarrollo sostenible.</li>
                        <li>Gestiona y promueve la conservación y uso sostenible de los ecosistemas terrestres.</li>
                        <li>Diseña métodos y técnicas para el estudio y gestión de los recursos naturales y la producción sostenible.</li>
                    </ul>
                    <p class="uk-text-bold">Actitudes y valores</p>
                    <ul class="uk-list uk-list-square">
                        <li>Respeto a la cosmovisión de los pueblos y su diversidad cultural.</li>
                        <li>Compromiso con la inclusión de los diversos sectores de la sociedad.</li>
                        <li>Demostrar principios éticos en el ejercicio profesional.</li>
                        <li>Respeto a la naturaleza con un compromiso y conciencia de conservación y preservación de los recursos naturales.</li>
                        <li>Poseer una actitud de diálogo y crítica constructiva para el manejo y resolución de conflictos Actitud ciudadana responsable en el ámbito profesional y en la sociedad.</li>
                    </ul>
                    <p class="uk-text-bold">Habilidades y destrezas</p>
                    <ul class="uk-list uk-list-square">
                        <li>Habilidad para liderar y trabajar en equipos multidisciplinarios.</li>
                        <li>Habilidad para desarrollar líneas de investigación y presentar informes con resultados para la solución de la problemática relacionada a la gestión de los recursos naturales y a la producción sostenible.</li>
                        <li>Habilidad para ofrecer alternativas de gestión integral de los recursos naturales para contribuir a resolver los problemas de seguridad alimentaria, la mitigación y adaptación al cambio climático.</li>
                        <li>Destreza para el diseño, ejecución y sistematización de proyectos y experiencias para el desarrollo rural.</li>
                        <li>Destreza y habilidades en el manejo de tecnología de información y comunicación en el desarrollo de proyectos de investigación y de desarrollo.</li>
                        <li>Capacidad crítica y analítica para proponer alternativas para la resiliencia de los sistemas de producción de alimentos en el país.</li>
                        <li>Habilidad para analizar y proponer la diversificación de los sistemas de producción sostenibles gestionando de manera integral los recursos naturales.</li>
                        <li>Habilidad para conectar y establecer las relaciones entre los sistemas naturales con los sistemas socioeconómicos, obteniendo un mutuo beneficio.</li>
                    </ul>
